/**
 * Determines whether a player is alive. Returns
 * a Boolean value.
 */
export const isAlive = (
	/**
	 * Player - The player whose life to check.
	 * Can use most player based Value Syntax to
	 * retrive this value.
	 * - `Type.Player.`
	 */
	player: string | number | any[]
) => {

	return `Is Alive(${player})`
}